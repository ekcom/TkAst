/**
 * Returns an element with the red flag icon
 */
export function getRedFlagIcon() {
	const imgEl = document.createElement("img");
	imgEl.src = "https://www.citypng.com/public/uploads/preview/hd-triangle-red-flag-illustration-transparent-png-11654166450hjezy4jve7.png";
	imgEl.style.width = "10px";
	return imgEl;
}

